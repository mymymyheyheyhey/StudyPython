# if 구문 - 흐름 제어
name = 'Elsa'

if name == 'John':
    print('How are you?')

elif name == 'Alice':
    print('Good morning')

else:
    print('I\'m fine, thanks.')

name = ['Elsa', 'John', 'Dorothy']

if 'John' in name:
    print('How are you?')

elif name == 'Alice':
    print('Good morning')

else:
    print('I\'m fine, thanks.')
